package com.ps.onedoor.DTO;

public class CategoryAdapterDTO {
    int image;
    String name;

    public CategoryAdapterDTO(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
